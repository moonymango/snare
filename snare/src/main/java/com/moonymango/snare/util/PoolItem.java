package com.moonymango.snare.util;

public abstract class PoolItem {

    //---------------------------------------------------------
    // static
    //---------------------------------------------------------

    // ---------------------------------------------------------
    // fields
    // ---------------------------------------------------------
    Pool<? extends PoolItem> mPool;
    boolean mIsRecycled = true;
    
    // ---------------------------------------------------------
    // constructors
    // ---------------------------------------------------------

    // ---------------------------------------------------------
    // methods
    // ---------------------------------------------------------
    public final boolean isRecycled() {
        return mIsRecycled;
    }
    
    public void recycle() {
        if (mPool != null) {
            mPool.recycle(this);
        }
    }
    
    // ---------------------------------------------------------
    // overrides
    // ---------------------------------------------------------

    // ---------------------------------------------------------
    // classes + interfaces
    // ---------------------------------------------------------
}
